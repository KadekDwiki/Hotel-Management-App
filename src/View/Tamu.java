    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tabelTamu.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(this, "Apakah yakin ingin menghapus data ini?", "Konfirmasi Hapus Data", JOptionPane.YES_NO_OPTION);
        
        if(confirm == JOptionPane.YES_OPTION){
            String id = tabelTamu.getValueAt(row, 0).toString();
            try{
                String query = "DELETE FROM tamu WHERE id=?";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1, id);
                int rowDeleted = ps.executeUpdate();
                
                if(rowDeleted > 0){
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                    refreshTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Data gagal dihapus");
                }
                
            } catch(Exception e){
                System.out.print("ERROR QUERY KE DATABASE:\n" + e + "\n\n");
            }
            
        }
        
    }//GEN-LAST:event_btnHapusActionPerformed

