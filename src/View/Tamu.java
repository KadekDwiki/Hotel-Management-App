    private void btnTambahDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahDataActionPerformed
       String nama = inputNama.getText();
       String noIdentitas = inputNoIdentitas.getText();
       String email = inputEmail.getText();
       String telepon = inputTelepon.getText();
       String alamat = inputAlamat.getText();
       
        try {
            if(!validateNoIdentitas()){
                return;
            }
       
            if(nama.isEmpty() || noIdentitas.isEmpty() || email.isEmpty() || telepon.isEmpty() || alamat.isEmpty() ){
                JOptionPane.showMessageDialog(this, "Semua field harus terisi");
            }
            else {  
                String query = "INSERT INTO tamu(nama, no_identitas, email, no_telp, alamat) VALUES (?,?,?,?,?)";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1, nama);
                ps.setString(2, noIdentitas);
                ps.setString(3, email);
                ps.setString(4, telepon);
                ps.setString(5, alamat);
                ps.executeUpdate();

                refreshTable();
                JOptionPane.showMessageDialog(this, "Data baru berhasil ditambahkan");

                inputNoIdentitas.setText("");
                inputNama.setText("");
                inputEmail.setText("");
                inputTelepon.setText("");
                inputAlamat.setText("");
                
                panelAddTamu.setVisible(false);
                panelTamu.setVisible(true);
            }
        } catch(Exception e) {
            System.out.print("ERROR QUERY KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_btnTambahDataActionPerformed

