    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        tm = new DefaultTableModel(null, new Object[]{"ID", "Nama", "Nomor Identitas", "Email", "Nomor Telepon", "Alamat"});
        tabelTamu.setModel(tm);
        tm.getDataVector().removeAllElements();
        
        String keyword = inputSearch.getText();
        try{
            String query = "SELECT * FROM tamu WHERE id LiKE ? OR nama LIKE ? OR no_identitas LIKE ? OR email LIKE ? OR no_telp LIKE ?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, "%" + keyword + "%");
            ps.setString(2, "%" + keyword + "%");
            ps.setString(3, "%" + keyword + "%");
            ps.setString(4, "%" + keyword + "%");
            ps.setString(5, "%" + keyword + "%");
            
            ResultSet result = ps.executeQuery();
            while(result.next()) {
                String id = result.getString("id");
                String nama = result.getString("nama");
                String noIdentitas = result.getString("no_identitas");
                String email = result.getString("email");
                String no_telp = result.getString("no_telp");
                String alamat = result.getString("alamat");
                
                Object[] data = {
                    id,
                    nama,
                    noIdentitas,
                    email,
                    no_telp,
                    alamat
                };
                
                tm.addRow(data);
            }
        }catch(Exception e) {
            System.out.print("ERROR QUERY KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_btnSearchMouseClicked

    public boolean validateNoIdentitas() {     
        boolean valid = false;
        String input = inputNoIdentitas.getText();
        String pesan = "Nomor Identitas sudah terdaftar\nSilahkan cek data tamu untuk lebih lanjut";
        
        try{
            String query = "SELECT * FROM tamu WHERE no_identitas = ?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, input);
            
            ResultSet result = ps.executeQuery();
            
            if(result.next()){
                JOptionPane.showMessageDialog(null, pesan, "Peringatan", JOptionPane.WARNING_MESSAGE);
            } else{
                valid = true;
            }
            
            
        }catch(Exception e) {
            System.out.print("ERROR QUERY KE DATABASE:\n" + e + "\n\n");
        }
        
        return valid;
    }
    
